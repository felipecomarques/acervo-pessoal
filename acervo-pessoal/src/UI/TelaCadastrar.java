    private void JB_CadastarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JB_CadastarActionPerformed
        if (JTP_LivrosDiscos.getSelectedIndex() == 0) {
            if ("".equals(JTF_NomeLivro.getText()) || "".equals(JTF_AutorLivro.getText())
                    || "".equals(JTF_EdicaoLivro.getText())) {
                JOptionPane.showMessageDialog(null, "É necessário preencher todos os dados!");
            } else {
                if (converter()) {
                    livros liv = new livros();
                    liv.cadastro(JTF_NomeLivro.getText(), JTF_AutorLivro.getText(), getEd());
                    JTF_NomeLivro.setText("");
                    JTF_AutorLivro.setText("");
                    JTF_EdicaoLivro.setText("");
                }
            }

        } else {
            if ("".equals(JTF_NomeDisco.getText()) || "".equals(JTF_AutorDisco.getText())) {
                JOptionPane.showMessageDialog(null, "É necessário preencher todos os dados!");
            } else {
                discos dvd = new discos();
                dvd.cadastro(JTF_NomeDisco.getText(), JTF_AutorDisco.getText(), JRad_Disco.isSelected());
            }
            JTF_NomeDisco.setText("");
            JTF_AutorDisco.setText("");
        }
    }//GEN-LAST:event_JB_CadastarActionPerformed

