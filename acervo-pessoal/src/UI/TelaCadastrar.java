    private boolean converter() {
        try {
            setEd(Integer.parseInt(JTF_EdicaoLivro.getText()));
            return true;
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "O campo 'Edição' deve ser composto "
                    + "de números! ");
            return false;
        }
    }

    public int getEd() {
        return ed;
    }

    public void setEd(int ed) {
        this.ed = ed;
    }

